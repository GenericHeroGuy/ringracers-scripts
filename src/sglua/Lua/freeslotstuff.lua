local firstsfx, firstspr, firststate, firstmt

local realfreeslot = freeslot
rawset(_G, "freeslot", function(...)
	local ret = {}
	for _, v in ipairs{...} do
		if pcall(do if _G[v] == nil then error() end end) then
			print(v.." already exists")
			table.insert(ret, _G[v])
			continue
		end
		local r = realfreeslot(v)
		rawset(_G, v, _G[v])
		table.insert(ret, r)
	end
	return unpack(ret)
end)

-- fun fact: skins actually have 33 sounds: 21 unused sounds from vanilla SRB2, plus 12 new sounds for ring racers
-- both of these are counted in NUMSKINSOUNDS, which means the game allocates 8415 freeslots for skinsounds, rather than the 3060 it actually needs
-- so technically, you actually have 6955 freeslots dedicated to SFX rather than 1600

COM_AddCommand("listfreeslots", function(p)
	local numsfx = 0
	for i = firstsfx, #sfxinfo - 1 do
		if sfxinfo[i].priority ~= 0 then
			numsfx = $ + 1
		else
			break
		end
	end

	local numspr = 0
	for i = firstspr, #sprnames - 1 do
		if tonumber(sprnames[i]) == nil then
			numspr = $ + 1
		else
			break
		end
	end

	local numstate = 0
	for i = firststate, #states - 1 do
		local s = states[i]
		--print(i..": "..s.sprite.." "..s.frame.." "..s.tics.." "..tostring(s.action).." "..s.var1.." "..s.var2.." "..s.nextstate)
		if not (s.sprite == 0 and s.frame == 0 and s.tics == 0 and s.action == nil and s.var1 == 0 and s.var2 == 0 and s.nextstate == 0) then
			numstate = i - firststate + 1
		end
		-- can't break the loop because some addons freeslot states without defining them
	end

	local nummt = 0
	for i = firstmt, #mobjinfo - 1 do
		local mt = mobjinfo[i]
		--print(i..": "..mt.doomednum.." "..mt.spawnstate.." "..mt.spawnhealth.." "..mt.seestate.." "..mt.seesound.." "..mt.reactiontime.." "..mt.attacksound.." "..mt.painstate.." "..mt.painchance.." "..mt.painsound.." "..mt.meleestate.." "..mt.missilestate.." "..mt.deathstate.." "..mt.xdeathstate.." "..mt.deathsound.." "..mt.speed.." "..mt.radius.." "..mt.height.." "..mt.dispoffset.." "..mt.mass.." "..mt.damage.." "..mt.activesound.." "..mt.flags.." "..mt.raisestate)
		if not (mt.spawnstate == 0 and mt.spawnhealth == 0 and mt.seestate == 0 and mt.seesound == 0 and mt.reactiontime == 0 and mt.attacksound == 0 and mt.painstate == 0 and mt.painchance == 0 and mt.painsound == 0 and mt.meleestate == 0 and mt.missilestate == 0 and mt.deathstate == 0 and mt.xdeathstate == 0 and mt.deathsound == 0 and mt.speed == 0 and mt.radius == 0 and mt.height == 0 and mt.dispoffset == 0 and mt.mass == 0 and mt.damage == 0 and mt.activesound == 0 and mt.flags == 0 and mt.raisestate == 0) then
			nummt = i - firstmt + 1
		end
		-- can't break here either
	end

	print("Freeslot usage:")
	print("SFX: "..numsfx.."/"..(#sfxinfo - firstsfx))
	print("Sprites: "..numspr.."/"..(#sprnames - firstspr))
	print("States: "..numstate.."/"..(#states - firststate))
	print("Mobjs: "..nummt.."/"..(#mobjinfo - firstmt))
	print("NOTE: Does not include vanilla SFX and sprite freeslots")
end, COM_LOCAL)

-- globalize and count SFX
print("Counting SFX")
for i = 1, #sfxinfo - 1 do
	local sfx = sfxinfo[i]
	--print(string.format("%s %05d %05d %05d %05d %s %s", tostring(sfx.singular), sfx.priority, sfx.flags, sfx.volume, sfx.skinsound, sfx.name, sfx.caption))
	if sfx.priority ~= 0 then
		firstsfx = i + 1
		--print(i..": sfx_"..sfx.name)
		if _G["sfx_"..sfx.name] == i then
			rawset(_G, "sfx_"..sfx.name, i)
		else
			print("WTF")
		end
	elseif firstsfx then
		break
	end
end

-- globalize and count sprites
print("Counting sprites")
for i = 0, #sprnames - 1 do
	local name = sprnames[i]
	if tonumber(name) == nil then
		--print(i..": SPR_"..name)
		if _G["SPR_"..name] == i then
			rawset(_G, "SPR_"..name, i)
			firstspr = i + 1
		else
			print("SPR_"..name.." is supposed to have ID "..i.." but it's actually "..tostring(_G["SPR_"..name])..". Dupe?")
		end
	end
end

-- globalize mobjs and states
print("Counting mobjs/states")
local function globalize(t)
	for k, v in pairs(t) do
		if _G[v] == k then
			rawset(_G, v, k)
		else
			print(v.." is supposed to have ID "..k.." but it's actually "..tostring(_G[v])..". Dupe?")
		end
	end
end

